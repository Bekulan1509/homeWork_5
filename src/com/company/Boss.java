package com.company;

public class Boss {
    private int health;
    private int attack;
    private int typeAbility;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getAttack() {
        return attack;
    }

    public void setAttack(int attack) {
        this.attack = attack;
    }

    public int getTypeAbility(int i) {
        return typeAbility;
    }

    public void setTypeAbility(int typeAbility) {
        this.typeAbility = typeAbility;
    }
}

